import streamlit as st
import pickle
import os
import numpy as np
from data_loader import extract_features, download_dataset
from model import train_and_save_model

# App Layout configuration
st.set_page_config(page_title="Deepfake Voice Detector", page_icon="🎙️", layout="centered")

st.title("🎙️ AI Deepfake Voice Recognition")
st.write("Upload an audio sample below to verify if the speech is authentic or AI-generated synthetic media.")

# Ensure the model file is generated/present
MODEL_PATH = "voice_deepfake_model.pkl"

if not os.path.exists(MODEL_PATH):
    st.info("Model file not found locally. Initializing Kaggle download and training pipeline... (This might take a minute)")
    with st.spinner("Downloading dataset and training AI model..."):
        train_and_save_model()
    st.success("Model trained successfully!")

# Load the trained model
@st.cache_resource
def load_classifier():
    with open(MODEL_PATH, 'rb') as file:
        return pickle.load(file)

model = load_classifier()

# File Uploader component
uploaded_file = st.file_uploader("Choose an audio file...", type=["wav", "mp3"])

if uploaded_file is not None:
    # Playback audio option
    st.audio(uploaded_file, format='audio/wav')
    
    # Save file temporarily to extract features
    temp_filename = "temp_user_input.wav"
    with open(temp_filename, "wb") as f:
        f.write(uploaded_file.getbuffer())
        
    with st.spinner("Analyzing audio frequencies and texture features..."):
        # 1. Process User Input
        features = extract_features(temp_filename)
        
        if features is not None:
            features = features.reshape(1, -1) # Reshape for single sample inference
            
            # 2. Generate Model Output (The Target)
            prediction = model.predict(features)[0]
            probabilities = model.predict_proba(features)[0]
            classes = model.classes_
            
            # Find probability index matching prediction
            pred_idx = np.where(classes == prediction)[0][0]
            confidence = probabilities[pred_idx] * 100
            
            st.markdown("---")
            st.subheader("Analysis Verdict:")
            
            # Custom styled output block based on result
            if prediction == "REAL":
                st.success(f"✅ **REAL**: The audio belongs to an actual human. (Confidence: {confidence:.2f}%)")
            else:
                st.error(f"🚨 **FAKE**: The audio is a synthetic, deepfake voice generated by AI. (Confidence: {confidence:.2f}%)")
        else:
            st.error("Could not process audio format. Please try another recording.")
            
    # Clean up local temporary audio file
    if os.path.exists(temp_filename):
        os.remove(temp_filename)